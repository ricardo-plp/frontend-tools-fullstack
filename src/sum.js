/**
 * The function exports a sum of two numbers in JavaScript.
 * @param a - The first parameter of the `sum` function.
 * @param b - The parameter `b` is the second number that will be added to the first number `a` in the
 * `sum` function.
 * @returns The sum of the two input numbers.
 */

/* eslint-env node */

function sum(a, b) {
    return a + b;
  }
  
  module.exports = sum;